# The count variable is the start of your count
count = 0

# The while loop tells you how many times the loop will go for.
while count < 3:
    print('Hello')
    # The next count will stop the loop. You have to have the
    # below statement to stop the loop.
    # The first time the loop starts the count at zero, then
    # move to number one, and then to two. The loop stop at three
    # because three is not greater than three in the while loop statement.
    count = count + 1